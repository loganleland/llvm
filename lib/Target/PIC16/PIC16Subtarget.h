#ifndef LLVM_LIB_TARGET_PIC16_PIC16SUBTARGET_H
#define LLVM_LIB_TARGET_PIC16_PIC16SUBTARGET_H

#include "PIC16FrameLowering.h"
#include "PIC16ISelLowering.h"
#include "PIC16InstrInfo.h"
#include "PIC16RegisterInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "PIC16GenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class PIC16Subtarget : public PIC16GenSubtargetInfo {
  virtual void anchor();
  bool ExtendedInsts;
  PIC16FrameLowering FrameLowering;
  PIC16InstrInfo InstrInfo;
  PIC16TargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  PIC16Subtarget(const Triple &TT, const std::string &CPU,
                  const std::string &FS, const TargetMachine &TM);

  PIC16Subtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const PIC16InstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const PIC16TargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};
} // End llvm namespace

#endif  // LLVM_TARGET_PIC16_SUBTARGET_H
